<h4>Можно ли при окраске стола из ясеня использовать такую схему: 1 слой колерованное масло BIOFA 2049 (цвет Х), 2 слой – колерованное масло BIOFA 2049 (цвет У)?</h4>
<p>
	 Схему, где последний слой масла BIOFA 2049 тоже колеруется мы не практикуем. Здесь действует общий принцип для поверхностей, которые будут периодически обновляться: паркетные продукты, подоконник, стол, и т. п.&nbsp; Принцип следующий - последний слой всегда должен быть бесцветным. И ухаживать, и обновлять потом нужно именно бесцветный слой. Таким образом можно проводить локальную реставрацию бесцветным маслом без нарушения целостности оптики всей поверхности. Если же локально нанести на поверхность цветное масло, то этот участок будет выделяться на общем фоне.
