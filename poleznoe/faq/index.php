	 Ну и третий вариант – есть шпаклевки, где в качестве наполнителя используется древесная пыль, такие шпаклевки окрашиваются в цвет колерованного масла. Шпаклевать пороки древесины нужно по "чистому" дереву. После окраски дефекты поверхности заделывают цветными восковыми карандашами.
