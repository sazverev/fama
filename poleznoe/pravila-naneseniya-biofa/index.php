<h2 style="text-align: justify;"><span style="font-size: 12pt;">Правило №2</span></h2>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
 <b>Шлифуйте поверхность абразивом с той зернистостью, которая указана в технических характеристиках масла</b>
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 Всегда читайте технические характеристики продукта перед использованием и следуйте им. Это гарантирует вам ожидаемый, обещанный производителем, результат.
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 Например, если заменить рекомендованный для использования абразив P-120 на абразив с зерном Р-80, то поверхность быстрее впитает масло. В результате расход увеличится, сама поверхность приобретет не ровный оттенок, а вместо того, чтобы образовать защитный слой, масло впитается внутрь дерева и будет плохо защищать от внешнего воздействия.
</p>
<p style="text-align: justify;">
</p>
<h2 style="text-align: justify;"><span style="font-size: 12pt;">Правило №3</span></h2>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
 <b>Наносите масло тонким слоем, словно растягиваете его по поверхности</b>
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 Тут главное помнить, что масло отличается от краски и наносить его нужно иначе. Ни в коем случае не используйте поролоновые губки. Используйте хорошие, не хлипкие, кисти, которые позволят «растягивать» покрытие по поверхности.
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 Одна из основных ошибок нанесения — слишком толстый слой. Иногда так делают в попытках получить более насыщенный тон. Но лучше сразу приобрести более темное масло и наносить его, «растягивая по поверхности». А покрывая твердую древесину маслом с высоким сухим остатком, обязательно располировывайте его по поверхности.&nbsp;
</p>
<p style="text-align: justify;">
</p>
<h2 style="text-align: justify;"><span style="font-size: 12pt;">Правило №4</span></h2>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
 <b>Ошибка: Процесс высыхания масла проходит без достаточного доступа воздуха.</b>
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 В результате поверхность долго не сохнет, продолжительное время ощущается специфический запах масла.
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 Процесс полимеризации масла происходит при взаимодействии с кислородом. Поэтому в помещение, где ведутся работы, необходимо обеспечить приток воздуха. После нанесения 1 слоя необходимо выдержать рекомендованное время сушки. Окончательная полимеризация происходит в течение 7 -10 дней. Все это время также необходимо регулярно проветривать помещение.
</p>
<p style="text-align: justify;">
</p>
<h2 style="text-align: justify;"> <span style="font-size: 12pt;">Правило №5</span></h2>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
 <b>Перед использованием перемешивайте и при необходимости процеживайте масло</b>
</p>
<p style="text-align: justify;">
	 Во время хранения и перевозки часть цветового пигмента опускается на дно банки. И если масло перед использованием масло хорошо не перемешать, то поверхность может получиться неравномерного цвета.
</p>
<h2 style="text-align: justify;"><span style="font-size: 12pt;">Правило №6</span></h2>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
 <b>Вагонку с профилем шип-паз окрашивайте до монтажа</b>
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 Это золотое правило, которое сбережет немало нервов при отделке. Если вагонку сначала смонтировать, а потом покрасить, то при высыхании будут проявляться неокрашенные участки шипа и портить внешний вид поверхности. Поэтому первый слой масла наносится до монтажа, а второй — после.
</p>
<p style="text-align: justify;">
</p>
<h2 style="text-align: justify;"> <span style="font-size: 12pt;">Правило №7</span></h2>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
 <b>Террасную доску и материал для обшивки фасада обрабатывайте со всех сторон</b>
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 Если вы обшиваете фасад дома материалами из дерева или монтируете террасную доску, то первый слой масла наносите на доску со всех сторон, а не только на лицевую. Это поможет выровнить влагообмен и дерево со временем не поведет. Так же доска будет защищена с внутренней стороны от насекомых-вредителей&nbsp;
</p>
<p style="text-align: justify;">
</p>
<h2 style="text-align: justify;"> <span style="font-size: 12pt;">Правило №8</span></h2>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
 <b>Старое лакокрасочное покрытие необходимо полностью удалить перед нанесением масла</b>
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 Эта ошибка ленивых мастеров может привести к тому, что работу надо будет полностью переделывать. Потому что если масло наносят на поверхность, которую до этого обрабатывали алкидными или другими составами, образующими пленку, то масло не впитается в поры древесины и совсем скоро начнет буквально отваливаться кусками.
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 Если на поверхность ранее наносили водные составы, то может получиться не тот оттенок цвета, который вы выбирали.
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 Поэтому поверхность, которую обрабатывают составами не в первый раз, перед нанесением масла BIOFA нужно отшлифовывать в соответствии с технологическими инструкциями.
</p>
<p style="text-align: justify;">
</p>
<h2 style="text-align: justify;"> <span style="font-size: 12pt;">Правило №9</span></h2>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
 <b>Не наносить ЛКМ на деревянные поверхности, обработанные хлорсодержащими веществами</b>
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 Хлорные отбеливатели используют для борьбы с плесенью и другими грибковыми поражениями. Но вместе с уничтожением вредителей они портят и верхний слой древесины, делая его мягким и рыхлым.
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 На такой поверхности любые лакокрасочные материалы долго держаться не будут. Поэтому мы рекомендуем пораженное дерево сначала отшлифовать, а потом нанести на него грунт 8750. Этот состав блокирует развитие грибковых заболеваний. Затем наносите масло 2043 или 1075, чтобы синева не просматривалась через финишный слой.
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 Если же хлорный отбеливатель уже был использован, то промойте поверхность дерева при помощи щетки и большого количества воды. Дайте ей просохнуть и проверьте, чтобы не оставалось высолов. Затем попробуйте отшлифовать небольшой участок и нанести на него сначала грунт 8750, а затем масло 2043. Если после полного высыхания вас устроит результат, то обрабатывайте всю поверхность. Но в этом случае мы не можем гарантировать 100%-ый результат.
</p>
<p style="text-align: justify;">
</p>
<h2 style="text-align: justify;"> <span style="font-size: 12pt;">Правило №10</span></h2>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
 <b>Используйте масла только для тех поверхностей, для которых они рекомендованы</b>
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 Если масло 9032 хорошо держится на полу, то почему бы им не покрасить, например, обеденный стол? Но даже если на полу нагрузка на масло больше, то на столешницу могут поставить горячий чайник или тарелку супа. Тогда на поверхности могут остаться следы, потому что состав 9032 не рассчитан на воздействие высоких температур.
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 Для столешниц рекомендуем специальный продукт — бесцветное масло 2052. Его нужно обязательно располировывать до суха падом (в крайнем случае грубой стороной губки для мытья). Если после располировки излишки масла, удалите их ветошью. Кроме того, это масло прошло тестирование и оно может взаимодействовать с пищевыми продуктами.
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 *указанные продукты отвечают европейскому стандарту &nbsp;EN&nbsp;73 ч.1, а это значит, что они ими можно покрывать игрушки. &nbsp;
</p>
<p style="text-align: justify;">
</p>
<h2 style="text-align: justify;"> <span style="font-size: 12pt;">Правило №11</span> </h2>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
 <b>Окрашивайте последний слой паркета прозрачным маслом</b>
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 Технология обработки паркета маслом заключается в том, что цвет ему придают первым слоем, а затем наносят 1-2 защитных, прозрачных слоя. И ухаживают или реставрируют именно верхний, бесцветный слой. Это позволяет делать даже локальную реставрацию незаметной.
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 А если сделать и верхний слой цветным, то при реставрации восстановленные участки будут сильно выделяться.
</p>
<p style="text-align: justify;">
</p>
<h2 style="text-align: justify;"> <span style="font-size: 12pt;">Правило №12</span> </h2>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
 <b>При влажной уборке покрытой маслом поверхности используйте средства с&nbsp;нейтральным PH</b>
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 Привычные нам моющие средства содержат щелочь, которая разрушает органические соединения и жиры и плохо влияют на натуральную краску.
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 Для очистки поверхностей, покрытых маслом&nbsp;BIOFA, лучше использовать средства с нейтральным&nbsp;PH, например,&nbsp;BIOFA&nbsp;4010 Nacasa. В его состав входят природные ПАВ из кокосового масла. К тому же, это средство безопасно для людей и домашних животных.
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 Техника безопасности
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<ul style="text-align: justify;">
	<li>
	<p>
		 Перед использованием ознакомиться с информацией на этикетке.
	</p>
 </li>
	<li>
	<p>
		 Хранить в недоступном для детей месте.
	</p>
 </li>
	<li>
	<p>
		 При проглатывании немедленно обратиться к врачу.
	</p>
 </li>
	<li>
	<p>
		 Продукт сам по себе не склонен к самовоспламенению.
	</p>
 </li>
	<li>
	<p>
		 Если рабочие материалы или одежда пропитались маслом, их необходимо хранить в плотно закрытых металлических емкостях. Либо хорошо промойте и высушите на негорючей поверхности - (Опасность самовозгорания!).
