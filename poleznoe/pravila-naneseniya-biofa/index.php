<h3 style="text-align: justify;"> <span style="font-size: 12pt;">Правило №1</span></h3>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
 <b><span style="font-size: 12pt;">Всегда отшлифовывайте поверхность перед использованием масла</span></b>
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 Если наносить темное масло на поверхность, которую предварительно не отшлифовали, то после покраски обязательно будут видны все дефекты обработки. Масло подчеркнет непростроги, задиры, ворсистость и др.
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 Кроме того, на не отшлифованной поверхности поры дерева закрыты, и масло проникает недостаточно глубоко. В результате концентрация антисептика меньше, из-за этого срок службы покрытия становится меньше и хуже проявляется рисунок древесины.
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 В приведенных ниже примерах мы предлагаем обратить внимание на правила работы и характерные ошибки, которые допускаю наши покупатели при работе с маслами&nbsp;BIOFA
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 Как правило, ошибки происходят в следующих случаях:
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<ul style="text-align: justify;">
	<li>
	<p>
		 неправильная подготовка поверхности;
	</p>
 </li>
	<li>
	<p>
		 нарушение технологии покраски;
	</p>
 </li>
	<li>
	<p>
		 неподходящий инструмент.
	</p>
 </li>
</ul>
