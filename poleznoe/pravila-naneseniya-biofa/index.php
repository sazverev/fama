	 Чтобы свойства любого отделочного покрытия раскрылись на все сто процентов, с ним нужно правильно работать. Поэтому мы подготовили для вас список основных правил, которые надо соблюдать, чтобы работа с маслами&nbsp;BIOFA приносила только удовольствие.
