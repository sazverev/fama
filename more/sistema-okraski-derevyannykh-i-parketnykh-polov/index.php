</p>
<p>
	 Второй слой прозрачное масло 8624 (8630).
</p>
<p>
	 Если пол новый , рекомендуем использовать 8630, если предстоит реставрация поверхности , лучше использовать 8624.
</p>
<p>
	 Вариант 5. Паркет из экзотических пород древесины (тик, венге, палисандр, кумару, ипе и подобные)
</p>
<p>
	 Особенность доски из экзотики заключается в том. что эти породы дерева уже насыщены природными маслами, поэтому здесь важно не нанести избыточный слой масла.
</p>
<p>
	 Для таких пород дерева применяют 3752 тиковое масло.
</p>
<p>
	 Этот продукт содержит в себе тунговое масло, придающее продукту 3752 свойство особенно глубоко проникать в древесину.
</p>
<p>
	 После шлифовки зерном Р-100-120, рабочая поверхность нескольких досок обрабатывается тканью смоченной растворителем 0500. Через три-четыре часа кистью или валиком наносится один тонкий слой 3752. ВАЖНО: На поверхности древесины не должно оставаться следов от малярного инструмента.
</p>
<p>
	 На следующий день проверяем, насколько хорошо масло впиталось в древесину- пробуем соскоблить ногтем масло на небольшом участке древесины. Если масло не соскабливается, можно наносить 3752 на всю площадь. Если же масло впиталось плохо, легко царапается и отделяется от поверхности, то значит, масло не впитывается в древесину. В этом случае шлифуем эти же экспериментальные доски более грубым абразивом, например, Р-80, протираем растворителем 0500 и снова тонко наносим 3752.
</p>
<p>
	 Теперь все должно получиться.
</p>
<p>
	 Когда после проверки на адгезию первого слоя 3752 получен удовлетворительный результат, на экспериментальные доски наносится второй слой 3752 с промежуточной шлифовкой первого слоя абразивом Р-240.На другой день проверяем состояние поверхности , и если все устраивает, наносим на всю площадь второй слой 3752.
</p>
<p>
	 НЕЛЬЗЯ наносить на 3752 другие паркетные продукты(9032, 8624, 5045 и т.д.)
</p>
<p>
	 Р.S.&nbsp; Дисковую машинку не рекомендуется использовать для нанесении 9032(9062), т.к. машинка плохо скользит по поверхности и слой получается очень тонким.
</p>
<h2>Как покрасить пол в помещениях с повышенной влажностью.</h2>
<p>
	 В домах и квартирах есть зоны с повышенной влажностью, на пол и стены здесь часто попадает вода. Это комнаты отдыха в бане, ванная комната, прихожая и т.д.
</p>
<p>
	 Для защиты деревянных поверхностей в этих помещениях мы предлагаем специальные продукты -&nbsp;BiofaВодный лак 5045(шелковисто-матовый) и 5245(матовый). Продукт создан из природных компонентов и представляет собой эмульсию растительных масел, смол и воска.
</p>
<p>
	 Если на пол, обработанный 5045/5245 пролить воду и вытереть образовавшиеся пятна через несколько часов, на поверхности не остается «следов» пролитой жидкости.
</p>
<p>
	 Стандартная схема покраски пола:
</p>
<ol>
	<li>Пол шлифуем абразивом Р-120, тщательно удаляем пыль и наносим один слой цветного масла из серии 8500. Если пол из сосны, то предварительно грунтуем&nbsp;Biofa&nbsp;3754 или обезжириваем поверхность растворителем&nbsp;Biofa&nbsp;0500.</li>
</ol>
<p>
	 После высыхания, через 16-24 часа, обязательно проводим межслойную шлифовку абразивом Р-500-600, не забываем удалить пыль и наносим слой бесцветного лака 5045/5245. Лак схватывается за 5-10 мин., поэтому работать нужно быстро. Лучше всего велюровым(микрофибры) валиком с ворсом до 4 мм. Чтобы избежать неравномерной окраски, в лак рекомендуется добавить 5-10 % замедлителя высыхания&nbsp;Biofa&nbsp;2146.
</p>
<p>
	 После полного высыхания, через 4-6 часов, опять проводим межслойную шлифовку абразивом Р-500-600 и наносим еще один слой 5045/5245.
</p>
<p>
	 Водный лак 5045/5245 можно нанести при помощи пульверизатора.
</p>
<p>
	 Наша компания использует водный лак для промышленной окраски инженерной доски в установках распылительного типа.
</p>
<h2>Этап 1. Предварительная обработка поверхности</h2>
<p>
	 Перед окраской с поверхность пола следует отшлифовать. Если полы имеют серьезные механические повреждения, требуется удалить старую краску, лак или выровнять поверхность, &nbsp;следует производить первоначальную шлифовку поверхности ленточной шлифовальной машиной. Используйте ленты абразивностью Р 40 - Р80.
</p>
 Окончательную шлифовку поверхности &nbsp;производят много- или однодисковой шлифовальной машиной, применяя абразивные круги Р 100-150. Для углов и труднодоступных мест используют &nbsp;ручную шлифовальную машину FESTOOL.<br>
<p>
	 Перед заключительным этапом шлифования следует произвести шпатлевание всех дефектов: трещин, сучков, щелей, если их размер превышает 0,5 мм. Производите шлифование поверхности в несколько этапов, используя абразивные материалы от грубого до тонкого. Тщательно удалите пыль с поверхности при помощи щетки и пылесоса.
</p>
<p>
	 Смолистые породы древесины следует обработать &nbsp;BIOFA 3754 Универсальная твердая грунтовка или растворителем BIOFA 0500, в противном случае возможно неравномерное окрашивание и образование темных пятен.
</p>
<h2>2 этап - Окраска</h2>
<p>
	 Перед нанесением цветного масла убедитесь, что обрабатываемая поверхность сухая (максимальная допустимая влажность 12%) очищена от грязи, пыли и жира и старых лакокрасочных покрытий. Температура в помещении не должна быть ниже 16 и выше 30°С.
</p>
<p>
	 Тщательно перемешайте продукт. Цветные масла из разных партий следует смешать друг с другом, чтобы не получить разницы в окрашиваемых поверхностях.
</p>
<p>
	 Для придания оттенка поверхности деревянного пола&nbsp; используйте цветное масло BIOFA Color-Oil For Indoors - не содержащий растворителей продукт, состоящий из высококачественных биомасел и восков. Нанесите масло тонким равномерным слоем&nbsp; при помощи валика с коротким ворсом или щетки для пола. Непосредственно после нанесения, следует распределить или убрать излишки масла с поверхности. Используйте для этого валик, щетку или белый пад (BIOFA 9961).
</p>
<p>
	 При нанесении масла однодисковой машиной, налейте небольшое количество масла в центр белого пада – так вы сможете избежать разбрызгивания. Установите на пад однодисковую&nbsp; шлифовальную машину. Нанесите цветное масло тонким равномерным слоем&nbsp; на всю поверхность пола. Через 15-20 минут после нанесения следует распределить или убрать излишки масла с поверхности. Используйте для этого белый пад. Своевременно&nbsp; производите замену пада. Насухо отполируйте поверхность пола с помощью белого пада. Старайтесь не допускать излишков масла и образования пленки на поверхности – весь материал должен впитаться в древесину.
</p>
<p>
	 Обеспечьте хорошую вентиляцию воздуха. В процессе сушки не допускайте попадания грязи, влаги и прямых солнечных лучей.
</p>
<p>
	 После высыхания первого слоя масла (через 16-24 часа) следует убедиться, что поверхность окончательно высохла – для этого можно использовать белую бумажную салфетку. Потрите ей окрашенную поверхность -&nbsp; на салфетке не должно остаться следов цветного масла. При низких температурах и повышенной влажности воздуха, время высыхания возрастает.
</p>
