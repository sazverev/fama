<h3>Как покрасить паркет</h3>
<p>
	 Вариант 1.&nbsp; Пол из сосны.
</p>
<p>
	 Шлифуем доску на финише абразивом с зернистостью Р-120.
</p>
<p>
	 Для выравнивания впитывающей способности древесины протираем всю поверхность пола таканью ( не красящейся) увлажненной растворителем 0500. Другими растворителями пользоваться не рекомендуем, т.к. они гораздо хуже справляются с обезжириванием &nbsp;поверхности. 1 литра растворителя хватает для обработки 50-70 м2.
</p>
<p>
	 Через&nbsp; 1 час можно приступать к нанесению &nbsp;цветного масла. Используем масло 8500 , наносим велюровым валиком. Можно использовать кисть или щетку для пола, но работать ими труднее. После нанесения слоя 8500 на сосну при шлифовке Р120 &nbsp;располировка не требуется.
</p>
<p>
	 &nbsp;Промываем валик в оставшемся растворителе .
</p>
<p>
	 На следующий день наносим валиком слой масла 9032(9062) , допускается добавление в масло 9032 до 10 % цветного масла, которое использовалось при нанесении первого слоя.
</p>
<p>
	 При работе с маслом 8511 или 8510 добавление белого масла во второй слой 9032(9062) ОБЯЗАТЕЛЬНО! В противном случае велика вероятность образования желтых пятен.
</p>
<p>
	 &nbsp;Через 16-24 часов накатываем третий слой 9032(9062) без всяких добавок. Предварительно, при необходимости, можно выполнить промежуточное шлифование поверхности сеткой Р600&nbsp; вручную.
</p>
<p>
	 &nbsp;Вариант 2. Пол из сосны. Очень много сучков , либо&nbsp; финишная шлифовка произведена&nbsp; абразивом с зернистостью Р-80-100.
</p>
<p>
	 &nbsp;Протираем пол отжатой влажной тканью смоченной в воде,&nbsp; удаляя частицы пыли оставшиеся после шлифовки.
</p>
<p>
	 Наносим валиком один слой грунтовки 3754. Грунтовка проникает в поры древесины и выравнивает впитывающую способность поверхности.
</p>
<p>
