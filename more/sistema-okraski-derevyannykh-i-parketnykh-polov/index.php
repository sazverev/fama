</p>
<p>
	 Для нанесения третьего и четвертого слоя используем 9032(9062). В предпоследний слой допускается добавить до 10 % масла 8500.
</p>
<p>
	 Вариант 3 (любительский).&nbsp; Пол из дуба, лиственницы, ясеня.
</p>
<p>
