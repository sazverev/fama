<h1 style="text-align: justify;">Покраска кухонных столешниц и разделочных досок</h1>
<p style="text-align: justify;">
</p>
<p>
	 Для кухонных столешниц мы предлагаем <a href="https://famaprofi.ru/catalog/dlya_vnutrennikh_rabot/mebel_i_stoleshnitsy/2052_maslo_dlya_rabochikh_poverkhnostey/">2052</a>&nbsp;в бесцветном варианте.
</p>
<p>
	 Дело в том, что поверхность столешниц подвержена серьезным механическим нагрузкам и требует довольно частого обновления. Проще всего вернуть поверхности первоначальное состояние можно именно при использовании бесцветного масла.
</p>
<p>
	 Поверхность предварительно шлифуется абразивом с зернистостью Р-150-180. Затем наносится два слоя масла.
</p>
<p>
 <b>Первый слой</b> наносим кисточкой/ валиком/ тканью ровным слоем с небольшим излишком. Ждем 30-40 мин и полируем обычной бытовой шлифовальной машиной&nbsp;орбитального&nbsp;типа с использованием&nbsp;зеленого или коричневого пада (для сосны коричневый или белый). Работать машинкой следует до тех пор, пока на поверхности не перестанут оставаться следы от пада.
</p>
<p>
 <b>Второй слой</b> наносим кисточкой через сутки. Ждем 20-30 мин. и располировываем белым падом
</p>
<p>
 <b>ВАЖНО:</b>&nbsp;Второй слой должен быть очень тонким, иначе поверхность может легко царапаться и долго не сохнуть (может вообще не высохнуть). Если такое произошло, т.е. поверхность уже целый месяц «липнет», нужно ручным шлифовальщиком с абразивом Р-240(или зеленым падом) убрать излишки масла (до сухой поверхности), нанести тонкий слой <a href="https://famaprofi.ru/catalog/dlya_vnutrennikh_rabot/mebel_i_stoleshnitsy/2052_maslo_dlya_rabochikh_poverkhnostey/">2052</a>, выждать 15 мин и насухо растереть полотенцем.
</p>
<p>
	 Восстановить поврежденную поверхность очень легко. Зачищаем поверхность наждачной бумагой Р 180. Наносим масло на поврежденный участок, делаем паузу в 20 мин., которая нужна для того, чтобы масло впиталось в поверхность дерева. Полируем белым падом, а излишки удаляем тканью.
</p>
<p>
	 Если нужно&nbsp;получить поверхность в цвете, можно поступить так: на столешницу из дуба, лиственницы или ясеня в качестве первого цветного слоя используем колерованное масло серии <a href="https://famaprofi.ru/catalog/dlya_vnutrennikh_rabot/8500_biofa_tsvetnoe_maslo_dlya_interera/">8500</a>.
</p>
<p>
	 Поверхность шлифуем абразивом с зерном Р- 150-180, наносим масло, ждем 30 мин и располировываем белым падом. После высыхания, лучше через пару дней, делаем легкое межслойное шлифование абразивом Р 500-600 и наносим два слоя бесцветного гидролака <a href="https://famaprofi.ru/catalog/dlya_vnutrennikh_rabot/pol_parket_lestnitsy/gidrolak_7010_profi_shelkovisto_matovyy_dlya_vnutrennikh_rabot/">7010</a>/<a href="https://famaprofi.ru/catalog/dlya_vnutrennikh_rabot/pol_parket_lestnitsy/gidrolak_dlya_drevesiny_7210_profi_matovyy_dlya_vnutrennikh_rabot/">7210</a> или бесцветного водного масла <a href="https://famaprofi.ru/catalog/dlya_vnutrennikh_rabot/5045_tverdoe_maslo_na_vodnoy_osnove_shelkovisto_matovoe/">5045</a>/<a href="https://famaprofi.ru/catalog/dlya_vnutrennikh_rabot/5245_tverdoe_maslo_na_vodnoy_osnove_matovoe/">5245</a>.
</p>
<p>
	 Время сушки первого слоя составляет 4-8 часов. После первого слоя тоже понадобится межслойное шлифование абразивом с зерном Р 500-600.
</p>
<p>
	 Очень хорошо наносить гидролак и водное масло валиком из микрофибры или полиамида с коротким (до 4 мм) ворсом. Работать нужно быстро, т.к. эти продукты очень быстро «схватывается».
</p>
<p>
	 Для сосны и бука такой вариант не очень подходит, потому что после нанесения <a href="https://famaprofi.ru/catalog/dlya_vnutrennikh_rabot/8500_biofa_tsvetnoe_maslo_dlya_interera/">8500</a> поверхность может пятнить.
</p>
<p>
	 В качестве финиша на столешницу, затонированную <a href="https://famaprofi.ru/catalog/dlya_vnutrennikh_rabot/8500_biofa_tsvetnoe_maslo_dlya_interera/">маслом 8500</a>, можно нанести один финишный слой бесцветного <a href="https://famaprofi.ru/catalog/dlya_vnutrennikh_rabot/mebel_i_stoleshnitsy/2052_maslo_dlya_rabochikh_poverkhnostey/">масла 2052</a>&nbsp;методом располировки.
</p>
<p>
</p>
<p>
 <b>Разделочные доски&nbsp;обрабатываются исключительно бесцветным </b><b><a href="https://famaprofi.ru/catalog/dlya_vnutrennikh_rabot/mebel_i_stoleshnitsy/2052_maslo_dlya_rabochikh_poverkhnostey/">маслом 2052</a></b><b>.</b> Поверхность предварительно нужно отшлифовать абразивом с зерном Р-150-180. Для располировки поверхности можно обойтись обычной губкой для мытья посуды. Использовать нужно грубую часть с абразивным материалом.
</p>
<p>
	 Масло на поверхность можно нанести при помощи тампончика из ткани. Полируем оба слоя насухо. Все масло должно впитаться в древесину без образования защитного слоя!
</p>
