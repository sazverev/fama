<h4><span style="color: #74ab37;">Можно ли разбавить BIOFA 5175/5177 водой?&nbsp;</span> </h4>
<p>
</p>
<p>
	 Разбавлять лазурь 5175/77 водой нежелательно, потому что может нарушиться стабильность водной эмульсии. По-простому, лазурь разделится на более густую и жидкую фракции. В результате, оттенки окрашенной поверхности будут различаться и ухудшится качество покрытия.
</p>
