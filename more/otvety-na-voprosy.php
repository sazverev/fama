<h4><span style="color: #74ab37;">Подскажите каким маслом или воском покрасить интерьер брусового дома из сосны, а потолок из лиственницы в белый цвет? Хочется именно белый с текстурой.</span> </h4>
<p>
	 Классический вариант для стен, потолков и предметов интерьера - масло BIOFA 2044, создает лессирующее (полупрозрачное покрытие). На потолок часто берут BIOFA 8101 аквалазурь белую, которая ложится более плотным слоем сохраняя при этом "рельеф" поверхности. Сучки при этом будут чуть-чуть проглядывать. Грунт на лиственницу не нужен.
</p>
<p>
	 Если нужен "выбеленный" вариант, можно попробовать масло BIOFA 8511. Продукт нанести на поверхность кистью с плотной и упругой натуральной щетиной, выждать 15 мин. и располировать белым падом, в крайнем случае можно просто тканью растереть. Падом получается лучше, богаче выглядит поверхность. Масло BIOFA 8511 довольно вязкое и им удобнее работать пока вагонка для потолка еще не смонтирована.
</p>
