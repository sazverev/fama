<h4><span style="color: #74ab37;">Обязательно ли грунтовать стены из сосны перед нанесением масла BIOFA 2044 или лазури BIOFA 5175?</span> </h4>
<p>
</p>
<p>
	 Нет, необязательно. При этом нужно учитывать следующее:
</p>
<p>
	 Сосна неравномерно впитывает масло и при работе с темными(«древесными») оттенками велика вероятность образования пятен. Поэтому мы рекомендуем под темные оттенки сосну загрунтовать, а вот под пастельные, светлые дизайны, этого можно не делать.
</p>
<p>
</p>
<h4><span style="color: #74ab37;">Планирую отделать стены вагонкой штиль, поверхность древесины на ощупь очень гладкая. Обязательно ли шлифовка стен перед нанесением ваших продуктов?</span> </h4>
<p>
</p>
<p>
	 При строжке древесины металлические ножи «приминают» верхний слой древесины и закрывают поры. При последующем нанесении водно-масляной лазури (и особенно масла) проникновение в поры будет, во-первых, затруднено, и древесина не получит должной защиты и, во-вторых, из-за очень тонкого слоя цвет получится ненасыщенным. Шлифовка же позволяет не только достичь определенной степени «гладкости» поверхности, но и открывает поры древесины.&nbsp; Учитывая то обстоятельство, что речь идет о внутренней отделке, шлифовка необязательна, но настоятельно рекомендуем купить пробник и оценить декоративную сторону вопроса.
</p>
<p>
</p>
