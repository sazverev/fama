<p>
	 Можно использовать те же самые продукты, что и для дома с отоплением, например, BIOFA 2044 или BIOFA 5175/5177. Но если есть опасение, что в доме будет повышенная влажность и другие неблагоприятные факторы, то можно использовать универсальный продукт, который годится как для внешних работ, так и для внутренних, например Лазурь для дерева BIOFA 1075.
