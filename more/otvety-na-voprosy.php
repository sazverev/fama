<h4><span style="color: #74ab37;">Сделал летом кирпичную пристройку к дому и обшил изнутри вагонкой. В ноябре на вагонке появилась плесень. Есть у вас специальные антисептики для внутренних помещений?</span> </h4>
<p>
</p>
<p>
	 Да, есть такое средство. BIOFA 1035 Nahos. Водный продукт, который не содержит биоцидов, а при нанесении на поверхность древесины модифицирует ее структуру (минерализует). При этом, древесина становится «несъедобной» для плесневых грибов. BIOFA 1035 совершенно безопасен для человека, его можно применять в кладовых с продуктами, ванных комнатах и помещениях бани. Через 72 часа после высыхания, на поверхность, обработанную BIOFA 1035 можно наносить защитные продукты BIOFA, предназначенные для внутренних работ.
</p>
