<h4><span style="color: #74ab37;">П</span><span style="color: #74ab37;"><span style="color: #74ab37;">о</span>дскажите, BIOFA 2087 цветной воск, не нашел в описании, есть ли защита от ультрафиолета?</span> </h4>
<p>
	 Цветной воск применяется для внутренних работ и специальные добавки для защиты от уф в рецептуру не входят. Задача защиты от уф излучения при внешних работах - защитить древесину от разрушения и фотодеструкции (посерения и почернения). Уф излучение внутрь дома через застекленные окна практически не попадает. Соответственно, древесина внутри дома от воздействия уф не разрушается и не чернеет. Минимизировать пожелтение поверхности поможет колеровка. Чем более плотный цвет, тем дольше сохранится первоначально выбранный дизайн.
</p>
<p>
</p>
<h4><span style="color: #74ab37;">Как лучше покрывать BIOFA 2087 вагонку на потолок? Первый слой до монтажа и второй уже на потолке? Два слоя на полу и уже полностью крашенную монтировать?</span> </h4>
<p>
</p>
<p>
	 Первый слой до монтажа обязательно: важно прокрасить шип, потому что если планки начнут ссыхаться, то этот неокрашенный элемент будет выделяться.
</p>
<p>
	 Второй слой тоже на земле- удобнее же, быстрее, лучше. Руки болеть не будут.
</p>
