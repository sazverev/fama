</p>
<h4><span style="color: #74ab37;">Вопрос про смолу. Сосна столярной влажности 6-8% но местами есть очень смолянистые участки. Как ляжет на них масло? Нужно ли чем-то обрабатывать их перед нанесением? Как думаете может ли смола потом "плакать" через масло?</span></h4>
<p>
	 На смолистые участки масло ляжет плохо, поэтому, такие участки нужно обработать растворителем 0500 PROFI. А правильнее, всю лицевую часть реек. Если смола выступает из смоляных кармашков, то будет "плакать" и потом, пока вся не выйдет. Если поверхность просто смолянистая, то в последствии смола может выходить на поверхность в виде мельчайших "капелек". На ощупь - как будто ворс на древесине поднялся. В этом случае удалить "капельки" легко - достаточно протереть поверхность тканью, смоченной растворителем 0500 PROFI. Если смола выделяется из кармашков, то вначале ее удаляют механическим способом, а затем протирают тканью, смоченной растворителем 0500 PROFI. Растворитель 0500 PROFI никак не влияет на окрашенную поверхность.
</p>
