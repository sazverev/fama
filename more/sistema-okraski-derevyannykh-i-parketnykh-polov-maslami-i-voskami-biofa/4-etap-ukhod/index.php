<h2><span style="font-size: 15pt;">Частичная реставрация деревянного пола, ранее обработанного маслом или воском.</span></h2>
<p>
</p>
<ul>
	<li>
	Восстановление оптики, удаление потертостей и мелких царапин</li>
</ul>
<p>
</p>
<p>
	 После 3-5 лет эксплуатации, либо интенсивных нагрузок, защитный слой масла истирается и для его восстановления и придания полу первоначальных оптических и защитных свойств необходимо провести обработку паркета при помощи Масло-ухода 2076. Продукт содержит воск и льняное масло, отлично удаляет потертости и царапины и готов к применению.
</p>
<p>
	 Масло-уход нужно нанести на пол максимально тонким слоем тканью или щеткой для пола и сразу же располировать белым падом. Расход -1 л/60 м2. Время высыхания 12 часов. Полная полимеризация происходит через 7 дней. Рекомендуем поручить выполнение этой работы профессиональной бригаде паркетчиков.
</p>
<p>
</p>
<ul>
	<li>
	Частичная реставрация </li>
</ul>
<p>
</p>
<p>
	 При экстремальных нагрузках и полного истирания защитного слоя прозрачного масла (либо протаптывания "дорожек" в локальных местах поверхности паркета рекомендуем нанести дополнительно один слой прозрачного масла BIOFA 9032/62, либо BIOFA 8624.
</p>
<p>
	 Работу легко выполнить самостоятельно. В случае локального нарушения защитного слоя допускается нанесения масла на отдельные участки. Со временем, оптика обработанного участка выравнивается с общим фоном всей площади поверхности.
</p>
<p>
</p>
<ul>
	<li>
	Реставрация сильно загрязненного пола </li>
</ul>
<p>
</p>
<p>
	 Поверхность приобрела сероватый или зеленоватый оттенки, сильно поблекла, но цвет тонировки сохранился.<br>
	 Очистить поверхность и освежить основной цвет можно без шлифовки используя 2057 Интенсивно чистящее масло. Затем нанести один из защитных прозрачных продуктов: твердый масло-воск BIOFA 9032(9062) или профессиональное масло BIOFA 8624.<br>
	 Эту работу лучше доверить профессионалам.
</p>
