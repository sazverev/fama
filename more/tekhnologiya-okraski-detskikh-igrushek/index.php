<h1>Технология окраски детских игрушек</h1>
<h6>
Для окраски детских игрушек мы предлагаем несколько продуктов и различных схем нанесения.&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;&nbsp;</h6>
<h3> <b><span style="color: #464646;">Вариант 1.</span></b>&nbsp;Окраска <a href="http://famaprofi.ru/catalog/dlya_vnutrennikh_rabot/steny_i_potolki/2087_tsvetnoy_vosk/"><span style="color: #f7941d;">Цветным воском BIOFA 2087</span></a> методом окунания.</h3>
<p>
	 Процесс окунания, как правило, выглядит следующим образом: в емкость с цветным воском, опускают в сетке(сите) деревянные элементы, затем эта сетка поднимается, и воск стекает обратно. Далее, элементы протираются от излишков цветного воска и развешиваются сушиться на крючках или просто раскладываются на хлопковую ткань (не цветную!).
</p>
<p>
	 В случае раскладывания игрушек для сушки на хлопковую ткань или иную поверхность следует обратить внимание, чтобы соприкосновение было со стороны поперечного среза древесины – больше впитываемость – меньше вероятность образования следов. Через час игрушки сухие.&nbsp;
</p>
<p>
	 Для получения ярких и укрывных цветов мы предлагаем цветовую гамму от дизайнеров немецкой компании GRIMM’S –производителя детских игрушек. В этой коллекции 24 насыщенных цвета.&nbsp; &nbsp;
</p>
<p>
	 При таком способе обработки, учитывая высокую концентрацию пигментных паст при соприкосновении (соударении, трении и т.п.) игрушек друг о друга, они оставляют друг на друге цветной след. То есть потерли зеленый кубик о красный и на красном кубике зеленая полоса – на зеленном наоборот, соответственно. Чтобы свести к минимуму этот эффект мы рекомендуем вариант 2.
