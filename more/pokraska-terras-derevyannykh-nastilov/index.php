<h2>Малярный инструмент</h2>
 <br>
 Специальная кисть для окраски полов маслами. Отлично распределяет масло по поверхности пола
<h3>Этап 1. Подготовка поверхности - шлифовка.</h3>
<p>
	 Поверхность шлифуют дважды: сначала абразивом с зернистостью 80 - 100 для формирования правильной геометрии и устранения дефектов черновой обработки. Затем второй раз - зернистостью 150 - 180, что создает оптимальное качество поверхности. После шлифовки поверхность протирают влажной тряпкой, удаляя пыль от шлифовки. Это дает долговрменную защиту древесины и оптимальный расход масла.
</p>
<h3>Этап 2. Окраска.</h3>
<p>
	 Масло для террас следует наносить кистью с плотной щетиной или щеткой для пола в два слоя тщательно растягивая по поверхности. Межслойная сушка составляет 16-24 часа. На окрашенной поверхности не должно быть пятен, переходов и полос от кисти. Придерживайтесь рекомендуемого расхода. Срок службы покрытия, которое эксплуатируется круглый год под открытым небом, составляет 1-3 года, в зависимости от климатических условий и механической нагрузки. Критерий для обновления – полная потеря блеска, поверхность «сухая», местами видны потертости, а вода, попавшая на поверхность, начинает впитываться в древесину.
</p>
<p>
	 Для обновления поверхность нужно очистить от загрязнения, желательно обработать абразивом Р 500, удалить пыль и нанести один слой используемого ранее колерованного продукта.
</p>
