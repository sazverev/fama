</p>
<p>
	 Обрабатывать доску будем <a href="https://famaprofi.ru/catalog/dlya_vneshnikh_rabot/besedki_pergoly_terassy_nastily/3753_maslo_dlya_terras/"><span style="color: #f7941d;">3753</span></a> маслом для террас.
</p>
<p>
	 Масло содержит в своем составе антисептик и УФ фильтр и имеет довольно вязкую консистенцию.
</p>
<p>
	 Первый слой наносится по всему периметру досок. Торцы обрабатываем этим же маслом или специальным средством для торцов - <a href="https://famaprofi.ru/catalog/dlya_vneshnikh_rabot/8403_zashchita_dlya_tortsov/"><span style="color: #f7941d;">8403</span></a>.
