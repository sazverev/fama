<h2>Система окраски</h2>
 <br>
<p>
	 Вы поставили новый сруб под крышу, а приступать к окраске еще рано по разным причинам: ранняя весна или поздняя осень, дом стоит на усадке или усушке, кончились деньги или не нашли подходящую бригаду.
</p>
<p>
	 Как временно защитить свежее дерево от атмосферных воздействий и биологических повреждений в виде синевы и плесени? Использовать специальный <a target="_blank" href="/catalog/dlya_vneshnikh_rabot/fasad_doma/8750_profi_grunt_antiseptik_dlya_dereva/"><b>грунт-антисептик 8750</b></a>!
</p>
<p>
	 Большая часть грунтов-антисептиков, встречающихся на рынке производятся на водной основе. Плюс таких составов – низкая стоимость. Но кроме антисептирования они не выполняют никакой другой функции, да, и к тому же под действием осадков очень быстро вымываются из древесины, если сверху их не защищать лакокрасочными материалами.
</p>
<p>
	 Альтернатива водным антисептикам – грунт-антисептик 8750 на основе льняного масла. Это универсальный продукт, который выполняет сразу несколько функций:
</p>
<ol>
	<li>Работает в качестве антисептика.&nbsp;Содержащиеся биоцидные добавки, проникая в поверхность, защищают древесину от развития гнили, плесени, грибков и синевы. Если на поверхности древесины уже есть поражения синевой, то грунт-антисептик их не уберет, зато остановит дальнейшее развитие.</li>
	<li>Защищает от атмосферных воздействий.&nbsp;Льняное масло, которое является основой данного грунта, глубоко проникает в древесину и пропитывает ее антисептиками. Одновременно образует на поверхности паропроницаемую пленку, которая защищает древесину от проникновения влаги, в виде дождя или снега.</li>
	<li>Снижает растрескивание древесины.&nbsp;Препятствуя проникновению внешней влаги, защитная пленка обеспечивает медленный и равномерный выход внутренней влаги из дерева, таким образом не происходит резкого изменения влажности древесины, что уменьшает внутренние напряжения и снижает эффект растрескивания.</li>
	<li>Уменьшает и выравнивает впитываемость поверхности, поэтому краска ложится без пятен и снижается ее расход&nbsp;(применительно к сосне, ели, кедру).</li>
</ol>
<p>
	 Особенность:&nbsp;можно работать при минимальной температуре +5 С до глубокой осени. Так же этот продукт может применяться на древесине влажностью до 40%.
</p>
<p>
</p>
<p>
	 Внимание! Только что срубленное дерево(сосна) имеет влажность около 60%. Причем неважно, срубили его зимой, весной или летом. Для того чтобы грунт хорошо впитался и держался на поверхности, бревно должно подсохнуть до 40 %.
</p>
<p>
	 Грунт антисептик является бесцветным составом и не может на длительный срок защитить древесину от выгорания, особенно в весенний и летний периоды, когда солнце наиболее агрессивно. Поэтому древесина с южной стороны может немного «загореть».
</p>
<p>
	 Отдельно стоит обратить внимание на обработку торцов деревянного дома. Торцевой срез - это поперечный срез капилляров дерева, т.е. тех трубочек, по которым питательные вещества подаются от корневой системы вверх по стволу. Через торцевой срез происходит быстрое насыщение и быстрый выход влаги, что неминуемо ведет к растрескиванию торцов. Для их защиты применяют специальное средство для торцов 8405, которые надежно изолируют торцы от влаги. А выход внутренней влаги происходит не через торцевой срез, а через тело бруса, таким образом, образование трещин снижается.
</p>
<p>
 <br>
	 Работы по консервации дома следует проводить в сухую погоду. Минимальная температура воздуха +5 С. Следует подгадать время так, чтобы в течение 8 часов после обработки не было дождя, это позволить грунту-антисептику глубоко проникнуть в древесину и создать надежную защиту.
</p>
<p>
	 В обычных условиях при окраске фасада деревянного дома, первым этапом идет шлифовка поверхности древесины. Шлифовкой убирают механические повреждения и открывают поры древесины после строжки. Дерево глубже впитывает масло, а цвет получается без пятен.
</p>
<p>
