</p>
<p>
	 При финишной окраски внутри дома, после использования антисептика 8750 менее чем 3-4 месяца назад, шлифовка поверхности обязательна.
</p>
<p>
	 Перед нанесением грунта 8750 очистите фасад дома от загрязнений при помощи жесткой щетки или ручного шлифовального инструмента.
</p>
<p>
	 Лучшим инструментом для нанесения масляного грунта-антисептика будет кисть. У любой древесины есть участки со слабой и с сильной впитываемостью. Работая кистью, вы, во-первых, втираете продукт в поверхность, обеспечивая лучшее проникновение. Во-вторых, переносите материал со слабо впитывающих участков на участки с повышенной впитываемостью.
</p>
<p>
	 Желательно использовать кисти с натуральным или смешенным ворсом. Выбирайте кисть с плотной набивкой – это позволит сэкономить время, т.к. плотная набивка кисти будет больше набирать в себя краски и вам не придется постоянно окунать кисть в банку. Так же в плотной кисти грунт будет лучше держаться и не капать на землю, пока вы несете кисть от банки до окрашиваемой поверхности. Ширину кисти следует выбирать в пределах 80-100 мм – это оптимальный размер для грунтования поверхности.
</p>
<p>
	 Также окраску можно производить и при помощи распылителя, в этом случае не забудьте позаботиться о надежной защите кожи, глаз и дыхательных путей.
</p>
<p>
