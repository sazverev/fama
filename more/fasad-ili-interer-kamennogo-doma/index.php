<h3 align="center">Нанесение краски на фасад</h3>
<p align="justify">
	 Фасадная краска FRONT FACADE готова к использованию и не требует разбавления. Перед началом работ краску необходимо тщательно перемещать и желательно отфильтровать. Дело в том, что при определенных условиях некоторое количество краски может высохнуть на внутренней стороне крышки и затем эти сухие остатки могут попасть в саму краску.
</p>
<p align="justify">
	 Для получения хорошего декоративного эффекта и достижения защитно-эксплуатационных свойств готового покрытия краску нужно нанести в два слоя. Окраску фасада можно проводить после полного высыхания грунта, но не менее чем через 4 часа. Работы необходимо производить в сухую погоду, окрашивание в дождливую погоду повлечет за собой неправильное высыхание и сокращение срока службы покрытия.
</p>
<p align="justify">
	 Для достижения декоративного эффекта и заявленных эксплуатационных характеристик температура воздуха и температура самого основания должна составлять не менее 8°C и не более 25°C, влажность воздуха должна находится в интервале 60 - 80%.
