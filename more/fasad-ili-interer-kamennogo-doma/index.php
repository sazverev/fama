	 Грунт наносится в ОДИН слой. Недогрунтование, как и перегрунтование желательно избегать. При недостатке грунта сильно уменьшается открытое время высыхания финишной шпаклевки или краски. Вода из финишного слоя моментально впитывается в основание и слой ЛКМ будет неправильно высыхать и станет хрупким. При излишнем количестве грунта получается «ламинированная» поверхность и вода из ЛКМ (шпаклевки) не проникает в основание и не «цепляется» за него, что может в дальнейшем привести к отслоению. Малярный инструмент (валик) может проскальзывать по такой поверхности и испортить декоративный эффект.<br>
 <br>
	 Правильно загрунтованная поверхность должна иметь равномерный слабо заметный блеск в отсутствии матовых пятен. При попытке провести по ней рукой, на ладони не должно оставаться строительной пыли или мела. При этом поверхность должна обладать слабой водоотталкивающей способностью (капли воды должны стекать по ней, а не впитываться моментально).<br>
