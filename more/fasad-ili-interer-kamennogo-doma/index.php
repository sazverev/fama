<h3 align="center">Подготовка к окрашиванию фасада</h3>
<p align="justify">
	 Перед нанесением краски, окрашиваемую поверхность (основание) необходимо подготовить. Если идет речь о перекраске дома, то первым делом нужно выяснить нет ли отслоения штукатурки от кирпичной кладки. Для этого вся площадь простукивается киянкой или деревянной ручкой молотка. В местах отслоения штукатурку удаляют, очищают от пыли, грунтуют и вновь оштукатуривают. Если все в порядке и отслоения штукатурки нет, то проверяют прочность ранее нанесенной шпаклевки (и краски). Участки со вздувшейся и отслаивающейся шпаклевкой выявляют при визуальном осмотре с помощью шпателя и зачищают их до прочного штукатурного слоя. Трещины «расшивают».<br>
</p>
<h3 align="center">Очистка фасада перед окраской</h3>
<p align="justify">
	 Затем следует очистить фасад жесткими металлическими щетками и вымыть стены водой мойкой высокого давления. Особое внимание нужно уделить участкам с высолами, жировыми пятнами, плесенью и зеленью. Такие участки нужно особенно тщательно обработать механическим способом, а затем обработать с применением соответствующих санитарных составов следуя рекомендациям производителя.
</p>
<p align="justify">
	 Если дом новый, то перед окраской его достаточно очистить от строительной пыли.<br>
 <br>
	 При нанесении шпаклевочного состава сначала шпателем заполняют впадины, трещины и неровности, дают шпаклевке высохнуть, а затем наносят основной слой, который выравнивают шпателем. При необходимости после схватывания шпатлевка шлифуется.
</p>
